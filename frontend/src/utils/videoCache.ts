  // ?? 怨꾩링 2: ?고???罹먯떆 ?뺤씤 ??????????????????????????????
  const runtime = runtimeCache.get(key)
  if (runtime) {
    const age = Date.now() - runtime.cachedAt
    if (age < RUNTIME_TTL_MS) {
      console.debug(`[videoCache] ?고???罹먯떆 ?덊듃 (${key}, ${Math.round(age / 1000)}s ?????`)
      return runtime
    }
    // TTL 留뚮즺 ???쒓굅 ??怨꾩링 1濡??댁뒪猷?    runtimeCache.delete(key)
    console.debug(`[videoCache] ?고???罹먯떆 TTL 留뚮즺 (${key})`)
  }

  // ?? 怨꾩링 1: ?뺤쟻 罹먯떆 ?뺤씤 ????????????????????????????????
  const staticUrl = STATIC_CACHE[key]
  if (staticUrl) {
    console.debug(`[videoCache] ?뺤쟻 罹먯떆 ?덊듃 (${key})`)
    return { url: staticUrl, source: 'static', cachedAt: Date.now() }
  }

  console.debug(`[videoCache] 罹먯떆 誘몄뒪 (${key}) ??FLOWAI ?몄텧 ?꾩슂`)
